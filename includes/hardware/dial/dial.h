// 가변저항 ADC 초기 세팅(채널, 그룹, 해상도... 설정)
static inline void initDial(void)
{
    initAdc(&PIN_DIAL);
}

// 가변저항 읽어온 값(0~4096 - 12bit 해상도)을 모터 각속도에 맞게 스케일링
static inline float32 getWRef(void)
{
    return getAdc(&PIN_DIAL) / MAX_ADC_VAL * 900;
}

