// 초음파 측정 명령 (HIGH - LOW - HIGH)
static inline void trigUltrasonic(void)
{
    setPinLow(PIN_US_TRIG);
    delayUs(2);
    setPinHigh(PIN_US_TRIG);
    delayUs(10);
    setPinLow(PIN_US_TRIG);
}

// echo 핀의 펄스 길이를 기반으로 거리 측정
static inline float32 getUsDist(void)
{
    float32 dist = ((float32)g_cnt_f / IfxStm_getFrequency(&MODULE_STM0) * 1000000.0f / 58.8235f);
    static float32 dist_prev;
    dist_prev = dist;

    return dist;
}

